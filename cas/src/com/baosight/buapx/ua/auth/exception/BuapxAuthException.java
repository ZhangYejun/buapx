package com.baosight.buapx.ua.auth.exception;

public class BuapxAuthException extends Exception  {

	public BuapxAuthException() {
	}

	public BuapxAuthException(String message) {
		super(message);
	}

	public BuapxAuthException(Throwable cause) {
		super(cause);
	}

	public BuapxAuthException(String message, Throwable cause) {
		super(message, cause);
	}

}
